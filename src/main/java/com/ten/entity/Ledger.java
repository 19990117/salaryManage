package com.ten.entity;

public class Ledger {
    private int id;
    private String year;
    private String empNum;
    private int base;
    private int payScale;
    private int goverSubsidy;
    private int nurRaises;
    private int teachAge;
    private int onlyChild;
    private int allSum;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getEmpNum() {
        return empNum;
    }

    public void setEmpNum(String empNum) {
        this.empNum = empNum;
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getPayScale() {
        return payScale;
    }

    public void setPayScale(int payScale) {
        this.payScale = payScale;
    }

    public int getGoverSubsidy() {
        return goverSubsidy;
    }

    public void setGoverSubsidy(int goverSubsidy) {
        this.goverSubsidy = goverSubsidy;
    }

    public int getNurRaises() {
        return nurRaises;
    }

    public void setNurRaises(int nurRaises) {
        this.nurRaises = nurRaises;
    }

    public int getTeachAge() {
        return teachAge;
    }

    public void setTeachAge(int teachAge) {
        this.teachAge = teachAge;
    }

    public int getOnlyChild() {
        return onlyChild;
    }

    public void setOnlyChild(int onlyChild) {
        this.onlyChild = onlyChild;
    }

    public int getAllSum() {
        return allSum;
    }

    public void setAllSum(int allSum) {
        this.allSum = allSum;
    }
}
